package com.cfjn.javacf.modle;

public class UserAccountInfo {

	private String loginName;

	private double totalIncome;

	private double totalExpend;

	private double surplus;

	private double budget;

	private double balance;

	private double financialAccount;

	private double totalBenefit;

	private double totalBenefitPercent;

	public double getTotalIncome() {
		return totalIncome;
	}

	public void setTotalIncome(double totalIncome) {
		this.totalIncome = totalIncome;
	}

	public double getTotalExpend() {
		return totalExpend;
	}

	public void setTotalExpend(double totalExpend) {
		this.totalExpend = totalExpend;
	}

	public double getSurplus() {
		return surplus;
	}

	public void setSurplus(double surplus) {
		this.surplus = surplus;
	}

	public double getBudget() {
		return budget;
	}

	public void setBudget(double budget) {
		this.budget = budget;
	}

	public double getBalance() {
		return balance;
	}

	public void setBalance(double balance) {
		this.balance = balance;
	}

	public double getFinancialAccount() {
		return financialAccount;
	}

	public void setFinancialAccount(double financialAccount) {
		this.financialAccount = financialAccount;
	}

	public double getTotalBenefit() {
		return totalBenefit;
	}

	public void setTotalBenefit(double totalBenefit) {
		this.totalBenefit = totalBenefit;
	}

	public String getLoginName() {
		return loginName;
	}

	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}

	public double getTotalBenefitPercent() {
		return totalBenefitPercent;
	}

	public void setTotalBenefitPercent(double totalBenefitPercent) {
		this.totalBenefitPercent = totalBenefitPercent;
	}

}
